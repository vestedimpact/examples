const goals = sdgs.map((goal) => {
  const { targets, ...rest } = goal;
  return rest;
});

const targets = sdgs.map((goal) => goal.targets.map((target) => ({ ...target, theme: goal.theme }))).flat();

export const SDGUtils = {
  allGoals: goals.map(({ goalNumber }) => goalNumber),
  getGoalColor: (sdgGoal: string) => {
    const sdgItem = goals.find(({ goalNumber }) => goalNumber === sdgGoal);
    if (!sdgItem) throw new Error('Invalid SDG goal number');
    return sdgItem.color;
  },
  getGoalLabel: (sdgGoal: string) => {
    const sdgItem = goals.find(({ goalNumber }) => goalNumber === sdgGoal);
    if (!sdgItem) throw new Error('Invalid SDG goal number');
    return sdgItem.goalLabel;
  },
  getGoalLabelSummary: (sdgGoal: string) => {
    const sdgItem = goals.find(({ goalNumber }) => goalNumber === sdgGoal);
    if (!sdgItem) throw new Error('Invalid SDG goal number');
    return sdgItem.goalLabelSummary;
  },
  getGoalTheme: (sdgGoal: string) => {
    const sdgItem = goals.find(({ goalNumber }) => goalNumber === sdgGoal);
    if (!sdgItem) throw new Error('Invalid SDG goal number');
    return sdgItem.theme;
  },
  getTargetLabel: (sdgTarget: string) => {
    const sdgItem = targets.find(({ targetNumber }) => targetNumber === sdgTarget);
    if (!sdgItem) throw new Error('Invalid SDG target number');
    return sdgItem.targetLabel;
  },
  getTargetNumberFromLabel: (label: string) => {
    const sdgItem = targets.find(({ targetLabel }) => targetLabel === label);
    if (!sdgItem) throw new Error('Invalid SDG target number');
    return sdgItem.targetNumber;
  },
  getTargetTheme: (sdgTarget: string) => {
    const sdgItem = targets.find(({ targetNumber }) => targetNumber === sdgTarget);
    if (!sdgItem) throw new Error('Invalid SDG target number');
    return sdgItem.theme;
  },
  sortGoals: (a: { sdgGoal: string }, b: { sdgGoal: string }) => {
    const itemA = goals.find(({ goalNumber }) => goalNumber === a.sdgGoal);
    const itemB = goals.find(({ goalNumber }) => goalNumber === b.sdgGoal);
    if (!itemA || !itemB) throw new Error('Invalid SDG goal number');
    return itemA.goalIndex - itemB.goalIndex;
  },
  sortTargets: (a: { sdgTarget: string }, b: { sdgTarget: string }) => {
    const itemA = targets.find(({ targetNumber }) => targetNumber === a.sdgTarget);
    const itemB = targets.find(({ targetNumber }) => targetNumber === b.sdgTarget);
    if (!itemA || !itemB) throw new Error('Invalid SDG target number');
    return itemA.targetIndex - itemB.targetIndex;
  },
};
